namespace SniffCore.Input
{
    /// <summary>
    ///     Represents the direction to be used for the up and down button in the <see cref="NumberBox" />.
    /// </summary>
    public enum UpDownDirections
    {
        /// <summary>
        ///     The button means step up.
        /// </summary>
        Up,

        /// <summary>
        ///     The button means step down.
        /// </summary>
        Down
    }
}